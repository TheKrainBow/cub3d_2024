#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*maillon;

	maillon = (t_list *)malloc(sizeof(t_list));
	if (!(maillon))
		return (0);
	maillon->content = content;
	maillon->next = NULL;
	maillon->prev = NULL;
	return (maillon);
}
